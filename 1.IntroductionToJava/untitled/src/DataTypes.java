public class DataTypes {

    public static void main(String[] args) {
        boolean isJavaFun = true;
        byte numCredits  = 3;
        char myGrade = 'A';
        short numCourses = 4;
        int age = 19;
        long tuitionPaid = 23983L;
        float gpa = 3.21f;
        double incomeEarned = 8283.98d;

        System.out.println("Boolean: " + isJavaFun);
        System.out.println("Byte: " + numCredits);
        System.out.println("Char: " + myGrade);
        System.out.println("Short: " + numCourses);
        System.out.println("Int: " + age);
        System.out.println("Long: " + tuitionPaid);
        System.out.println("Float: " + gpa);
        System.out.println("Double: " + incomeEarned);

    }
}
