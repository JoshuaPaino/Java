public class ArithmeticOperators {

    public static void main(String[] args) {
        int num1 = 20, num2 = 10, result;
        result = num1 + num2;
        System.out.println("num1 + num2 = " + result);

        double numA = 20D, numB = 10D, res;
        res = numA - numB;
        System.out.println("numA - numB = " + res);

        float numFA = 10F, numFB = 10F, resF;
        resF = numFA * numFB;
        System.out.println("numFA * numFB = " + resF);


    }
}
