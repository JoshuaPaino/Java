public class AssignmentsOperators {
    public static void main(String[] args) {
        int varInt1 = 10, varInt2 = 5,varInt3 = 5;
        System.out.println("varInt1 = " + varInt1);
        System.out.println("varInt2 = " + varInt2);
        System.out.println("varInt3 = " + varInt3);
        varInt1 += 1;
        varInt2++;
        varInt3--;
        System.out.println("varInt1 after an increment" + varInt1);
        System.out.println("varInt2 after an increment " + varInt2);
        System.out.println("varInt3 after an increment " + varInt3);



    }
}

